import ApiClient from "../../api-client";
import { apiUrl } from "../../environment";
import { toast } from "react-toastify";
import 'react-toastify/dist/ReactToastify.css';
toast.configure();

// PROGRAM Type ACTIONS
export const list_assigned_questionnaire = (params) => {
  return (dispatch, getState) => {
    const { user: { token } } = getState();
    dispatch({ type: "ISLOADING", data: true });
    ApiClient.post(`${apiUrl}3001/questionnaire/assigned-questions`, params, token).then((response) => {
      dispatch({ type: "ISLOADING", data: false });
      toast.dismiss();
      if (response.messageID === 200) {
        dispatch({ type: "LIST_ASSIGNED_QUESTIONNAIRE", data: response.data });
      } else if (response.messageID === 404) {
        toast.warn(response.message);
      } else {
        toast.error(response.message);
      }
    });
  }
};


export const create_assigned_questionnaire = (data) => {
  return (dispatch, getState) => {
    const { user: { token } } = getState();
    dispatch({ type: "ISLOADING", data: true });
    ApiClient.post(`${apiUrl}3001/program/lesson-questionnaire`, data, token).then((response) => {
      dispatch({ type: "ISLOADING", data: false }); toast.dismiss();
      toast.dismiss();
      if (response.messageID === 201) {
        toast.success(response.message);
        if (response.data) dispatch({ type: "ADD_ASSIGNED_QUESTIONNAIRE", data: response.data });
      } else if (response.messageID === 404) {
        toast.warn(response.message);
      } else {
        toast.error(response.message);
      }
    });
  }
}